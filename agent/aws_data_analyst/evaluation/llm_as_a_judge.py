from strands import Agent
from strands.models import BedrockModel

from json_repair import repair_json


MODEL_ID = "global.anthropic.claude-haiku-4-5-20251001-v1:0"
TEMPERATURE = 0.2

SYSTEM_PROMPT = """You are an economist tasked with evaluating the coreectness of an answer generated by an AI Agent.
You will receive the input User Question, the Expected Answer, and the Generated Answer.

The expected answer will be typically more succinct than the generated one.
You will have to check that the Generated Answer contains the information provided in the Expected Answer.
Moreover, you will have to check that any provided extra information in the Generated Answer report merely factual information, without expressing any subjective opinion.

You will provide an overall score from 0 to 1:
* 0: if the Generated Answer does not contain the information provided in the Expected Answer, or whether it provides subjective opinions.
* 1: if the Generated Answer contains the information provided in the Expected Answer, and any extra information is factual and objective.

You should return a single JSON object with the following format:
{
    "score": 0 to 1,
    "rationale": "rationale for the provided score"
}
"""


class DataAnalyst_Evaluator:
    def __init__(self):
        self.agent = Agent(
            model = BedrockModel(
                model_id=MODEL_ID,
                temperature=TEMPERATURE,
            ),
            system_prompt=SYSTEM_PROMPT,
            tools=[
            ],
            callback_handler=None)

    def evaluate(self, question, expected_answer, generated_answer, cached=True):
        response = self.agent(f"User Question: {question}\nExpected Answer: {expected_answer}\nGenerated Answer: {generated_answer}")
        return repair_json(str(response), return_objects=True)
